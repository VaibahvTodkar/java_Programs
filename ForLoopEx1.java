public class ForLoopEx1 {
    public static void main(String[] args) {
        int i = 0 ;
            
            System.out.println("Post Increment:"+ i++);
            int j = 0;
            System.out.println("Pre Increment :"+ ++j);

            System.out.println("Post decrement:"+ i--);
            j = 0;
            System.out.println("Pre decrement :"+ --j);
        
    }
}
